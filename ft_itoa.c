#include "libft.h"

char		*ft_itoa(int n)
{
	char	*s;
	int		sign;

	s = (char *)malloc(sizeof(char) * (size_t)12);
	if (NULL == s)
		return (NULL);
	if (n == 0)
		return ("0");
	else if (n == -2147483648)
		return ("-2147483648");
	(n < 0) ? sign = 1 : 0;
	(n < 0) ? n = -n : 0;
	*(s + 12) = '\0';
	while (n)
	{
		*(--s) = ('0' + (n % 10));
		n /= 10;
	}
	(sign == 1) ? *(--s) = '-' : 0;
	return (s);
}
